/********************************************************************************
  Description:	Write sensor register to change exposure time
  Parameter: t -- exposure time clock num 
*********************************************************************************/
void V558_SifSetSnrTime(UINT8 freqtype, UINT32 t)
{
	UINT16 uTemp = 0;

	UINT16 vBlank = 0;
	UINT16 times = 0;

	V558_I2C_BATCH AeData[2];

	if(freqtype) //60hz
	{
		times = t/100000;
	}
	else //50hz
	{
		times = t/120000;
	}

	if(times > 3)
	{
		vBlank = t/780-470;
	}
	else
	{
		vBlank = 0x2d;
	}
	
	V558_SifI2cWriteByte(0x14,(UINT8)(vBlank>>8)); 
	V558_SifI2cWriteByte(0x15,(UINT8)(vBlank)); 
	times = 1000;
	while(times--);
//	V558_Delay(5); 

	//AeData[0].RegBytes = 2;
	//AeData[0].RegAddr = 0x14;
	//AeData[0].RegVal[0]= (UINT8)(vBlank>>8);
	//AeData[0].RegVal[1]= (UINT8)(vBlank);

	uTemp = (UINT16)(t/780+1); 
	AeData[1].RegBytes = 2;
	AeData[1].RegAddr = 0xd;
	AeData[1].RegVal[0]= (UINT8)(uTemp>>8);
	AeData[1].RegVal[1]= (UINT8)(uTemp);
	V558_SifI2cAeBatch(1, &AeData[1]);
}

